const LangStore = {
  KEY: "carrental.lang",
  get(){ return localStorage.getItem(this.KEY) || "en"; },
  set(v){ localStorage.setItem(this.KEY, v); }
};

function t(key){
  const lang = LangStore.get();
  return (I18N[lang] && I18N[lang][key]) || I18N.en[key] || key;
}

function applyTranslations(){
  const lang = LangStore.get();
  document.documentElement.lang = lang;
  document.documentElement.dir  = (lang === "ar") ? "rtl" : "ltr";

  document.querySelectorAll("[data-i18n]").forEach(el => {
    const key = el.getAttribute("data-i18n");
    const val = t(key);
    if (el.tagName === "TITLE") document.title = val;
    else el.textContent = val;
  });
  // Translate the `title` attribute for elements with data-i18n-title
  document.querySelectorAll("[data-i18n-title]").forEach(el => {
    el.title = t(el.getAttribute("data-i18n-title"));
  });
  // Translate the `placeholder` attribute for elements with data-i18n-placeholder
  document.querySelectorAll("[data-i18n-placeholder]").forEach(el => {
    el.placeholder = t(el.getAttribute("data-i18n-placeholder"));
  });

  // toggle button active state
  document.getElementById("btn-en")?.classList.toggle("active", lang === "en");
  document.getElementById("btn-ar")?.classList.toggle("active", lang === "ar");

  // notify the rest of the app so dynamic content can re-render
  document.dispatchEvent(new CustomEvent("lang:changed", { detail: { lang }}));
}

function setLang(lang){
  LangStore.set(lang);
  applyTranslations();
}

document.addEventListener("DOMContentLoaded", () => {
  document.getElementById("btn-en")?.addEventListener("click", () => setLang("en"));
  document.getElementById("btn-ar")?.addEventListener("click", () => setLang("ar"));
  applyTranslations();
});
